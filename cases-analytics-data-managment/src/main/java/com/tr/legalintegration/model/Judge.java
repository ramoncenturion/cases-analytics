package com.tr.legalintegration.model;

public class Judge {

	private String id;
	private String name;
	private String country;
	private String subject;
	private String caratula;
	private String court;
	private String demanding;
	private String defendant;
	private String source;
	private String idVeredict;
	private String dateVeredict;
	private String veredict;
	
	
	
	
	
	//GETTER AND SETTER
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getCaratula() {
		return caratula;
	}
	public void setCaratula(String caratula) {
		this.caratula = caratula;
	}
	public String getCourt() {
		return court;
	}
	public void setCourt(String court) {
		this.court = court;
	}
	public String getDemanding() {
		return demanding;
	}
	public void setDemanding(String demanding) {
		this.demanding = demanding;
	}
	public String getDefendant() {
		return defendant;
	}
	public void setDefendant(String defendant) {
		this.defendant = defendant;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getIdVeredict() {
		return idVeredict;
	}
	public void setIdVeredict(String idVeredict) {
		this.idVeredict = idVeredict;
	}
	public String getDateVeredict() {
		return dateVeredict;
	}
	public void setDateVeredict(String dateVeredict) {
		this.dateVeredict = dateVeredict;
	}
	public String getVeredict() {
		return veredict;
	}
	public void setVeredict(String veredict) {
		this.veredict = veredict;
	}
	
	
}
